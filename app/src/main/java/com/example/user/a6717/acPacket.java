public class acPacket {
    double x;
    double y;
    double z;
    public acPacket(double a, double b, double c) {
        x=a;
        y=b;
        z=c;
    }
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public double getZ() {
        return z;
    }

}
